package com.yp.ollama.controller;

import com.yp.ollama.dto.ChatRequest;
import com.yp.ollama.dto.ChatResponse;
import com.yp.ollama.service.ChatService;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/api/v1/chat")
public class ChatController {
    private final ChatService chatService;

    public ChatController(ChatService chatService) {
        this.chatService = chatService;
    }

    @PostMapping
    public ChatResponse chat(@RequestBody ChatRequest chatRequest) {
        ChatResponse response = chatService.chat(chatRequest);
        response.setResponse(response.getFormattedResponse());
        return response;
    }
}
